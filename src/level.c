int current_level = 0;

short (*current_level_map)[MAP_WIDTH] = level0_map; // default Stage 0

void change_level(int level)
{
  switch (level)
  {
  case 1:
    current_level_map = level1_map;
    break;
  case 0:
  default:
    current_level_map = level0_map;
    break;
  }
  current_level = level;
}

void render_level(SDL_Renderer *renderer)
{
  for (int y = 0; y < MAP_HEIGHT; y++)
  {
    for (int x = 0; x < MAP_WIDTH; x++)
    {
      SDL_FRect tile_rect = {x * TILE_SIZE, y * TILE_SIZE, TILE_SIZE, TILE_SIZE};

      switch (current_level_map[y][x])
      {
      case PLATFORM:
        SDL_SetRenderDrawColor(renderer, 161, 35, 30, 255);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case SOLID_OBSTACLE:
        SDL_SetRenderDrawColor(renderer, 161, 66, 11, 255);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case SWITCH:
        SDL_SetRenderDrawColor(renderer, 247, 212, 65, 255);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case COINS:
        SDL_SetRenderDrawColor(renderer, 0, 40, 30, 255);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case EXIT_GATE:
        SDL_SetRenderDrawColor(renderer, 255, 255, 255, 255);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      default:
        break;
      }
    }
  }
}

bool is_solid(Transform *transform) {
  int left = transform->x / TILE_SIZE;
  int right = (transform->x + transform->w - 1) / TILE_SIZE;
  int top = transform->y / TILE_SIZE;
  int bottom = (transform->y + transform->h - 1) / TILE_SIZE;

  for (int y = top; y <= bottom; y++) {
      for (int x = left; x <= right; x++) {
          if (x < 0 || x >= MAP_WIDTH || y < 0 || y >= MAP_HEIGHT) {
              return false;
          }
          for (size_t i = 0; i < sizeof(solid_tiles) / sizeof(solid_tiles[0]); i++) {
              if (current_level_map[y][x] == solid_tiles[i]) {
                  return true;
              }
          }
      }
  }
  return false;
}

bool is_void(Transform *transform)
{
  return transform->y + transform->h > TILE_SIZE * MAP_HEIGHT;
}

bool is_exit(Transform *transform)
{
  int left = transform->x / TILE_SIZE;
  int right = (transform->x + transform->w - 1) / TILE_SIZE;
  int top = transform->y / TILE_SIZE;
  int bottom = (transform->y + transform->h - 1) / TILE_SIZE;

  for (int y = top; y <= bottom; y++) {
      for (int x = left; x <= right; x++) {
          if (x < 0 || x >= MAP_WIDTH || y < 0 || y >= MAP_HEIGHT) {
              return false;
          }
          for (size_t i = 0; i < sizeof(solid_tiles) / sizeof(solid_tiles[0]); i++) {
            if (current_level_map[y][x] == EXIT_GATE){
              return true;
            }
          }
      }
  }
  return false;
}