void render_level(SDL_Renderer *renderer)
{
  bool gate_found = false;
  for (int y = 0; y < MAP_HEIGHT; y++)
  {
    for (int x = 0; x < MAP_WIDTH; x++)
    {
      SDL_FRect tile_rect = {x * TILE_SIZE, y * TILE_SIZE, TILE_SIZE, TILE_SIZE};

      switch (current_level_map[y][x])
      {
      case PLATFORM:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case SOLID_OBSTACLE:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case SWITCH:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case COINS:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        draw_coin(renderer, tile_rect, COINS);
        break;
      case EXIT_GATE:
        SDL_SetRenderDrawColor(renderer, 255, 255, 255, 255);
        SDL_RenderFillRect(renderer, &tile_rect);
        // render pinggiran gate 1 kali saja
        if (!gate_found)
        {
          // render gate untuk melapisi kotak putih
          draw_gate(renderer, tile_rect);
          gate_found = true;
        }
        break;
      case BUTTON:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        SDL_RenderFillRect(renderer, &tile_rect);
        break;
      case SAWS:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        draw_rotating_saw(renderer, tile_rect, SDL_GetTicks() / 2 % 360);
        break;
      case SPIKE:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        draw_triangle(renderer, tile_rect);
        break;
      case FAKE_COINS:
        SDL_SetRenderDrawColor(renderer, current_level->foreground_color.r, current_level->foreground_color.g, current_level->foreground_color.b, current_level->foreground_color.a);
        draw_coin(renderer, tile_rect, FAKE_COINS);
        break;
      default:
        break;
      }
    }
  }
}

void insert_level(LevelNode **head, LevelNode *level)
{
  if (*head == NULL)
  {
    // If list is empty, make this the first level
    *head = level;
    level->prev = NULL;
    level->next = NULL;
    return;
  }

  LevelNode *temp = *head;
  LevelNode *prev_match = NULL;
  LevelNode *next_match = NULL;

  // First pass: find where this level should be inserted
  while (temp != NULL)
  {
    // Check if current node is the previous of our new level
    if (strcmp(temp->name, level->prev_name) == 0)
    {
      prev_match = temp;
    }
    // Check if current node is the next of our new level
    if (strcmp(temp->name, level->next_name) == 0)
    {
      next_match = temp;
    }
    temp = temp->next;
  }

  // Second pass: if no matches found, append to end
  if (prev_match == NULL && next_match == NULL)
  {
    temp = *head;
    while (temp->next != NULL)
    {
      temp = temp->next;
    }
    temp->next = level;
    level->prev = temp;
    level->next = NULL;
    return;
  }

  // Handle cases where we found matches
  if (prev_match != NULL)
  {
    // Insert after prev_match
    level->prev = prev_match;
    level->next = prev_match->next;
    if (prev_match->next != NULL)
    {
      prev_match->next->prev = level;
    }
    prev_match->next = level;
  }

  if (next_match != NULL)
  {
    // Insert before next_match
    level->next = next_match;
    level->prev = next_match->prev;
    if (next_match->prev != NULL)
    {
      next_match->prev->next = level;
    }
    else
    {
      // If inserting at head, update head pointer
      *head = level;
    }
    next_match->prev = level;
  }
}

void goto_level_by_name(LevelNode *head, const char *name)
{
  LevelNode *current = head;

  while (current != NULL)
  {
    if (strcmp(current->name, name) == 0)
    {
      current_level = current;
      return;
    }
    current = current->next;
  }

  change_level();
}

void goto_next_level()
{
  current_level = current_level->next;
  change_level();
}

void goto_prev_level()
{
  if (current_level->prev == NULL)
  {
    SDL_Log("Already at the first level.");
    return;
  }
  current_level = current_level->prev;
  change_level();
}
